public class Main {
    public static void main(String[] args) {

        // while döngü örneği

        int i = 1;
        while (i<10){
            System.out.println(i);
            i++;
        }
        System.out.println("While Döngüsü Bitti");


        // Do-While döngüsü

        int j = 1;
        do {
            System.out.println(j);
            j++;
        }while (j<10);

        System.out.println("Do-While Döngüsü Bitti");
    }
}

// While döngüsü ile Do-While döngüsü arasında ki fark.
// Şart uymazsa While Döngüsü çalışmaz.
// Şart uymazsa Do-While döngüsü en az bir kere çalışır.


